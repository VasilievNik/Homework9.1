package Task1;

public class Author {
    String Name;
    String Sername;
    public Author(String Name, String Sername){
        this.Name = Name;
        this.Sername = Sername;
    }

    public String getAuthorName() {
        return this.Name;
    }
    public String getAuthorSername() {
        return this.Sername;
    }

}
